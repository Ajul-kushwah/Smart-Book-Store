    private void createMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_createMouseClicked
        // TODO add your handling code here:
        if (nameTF.getText().equals("") && passwordTF.getText().equals("") && SecQ.getText().equals("") && answerTF.getText().equals("")){
            javax.swing.JOptionPane.showMessageDialog(null, "Your name,password,sequrity question and answer fields is still empty!");
            username.requestFocus();
        }else if (!nameTF.getText().equals("")  && passwordTF.getText().equals("") && SecQ.getText().equals("") && answerTF.getText().equals("")){
            javax.swing.JOptionPane.showMessageDialog(null, "Your  password,sequrity question and answer fields is still empty!");
            username.requestFocus();
        }else if (!nameTF.getText().equals("")  && !passwordTF.getText().equals("") && SecQ.getText().equals("") && answerTF.getText().equals("")){
            javax.swing.JOptionPane.showMessageDialog(null, "Your sequrity question and answer fields is still empty!");
            passwordTF.requestFocus();
        }else if ( SecQ.getText().equals("") && !answerTF.getText().equals("")){
            javax.swing.JOptionPane.showMessageDialog(null, "Your security question field is still empty!");
            passwordTF.requestFocus();    
        }else if (answerTF.getText().equals("")){
            javax.swing.JOptionPane.showMessageDialog(null, "Your answer field is still empty!");
            passwordTF.requestFocus();      
        } else{
        
            conn = DatabaseConnection.ConnectDB();
        
       try{
            
            String sql = "Insert into signup (username,password,security_question,answer) values (?,?,?,?)";
            pst=conn.prepareStatement(sql);
            pst.setString(1, nameTF.getText());
            pst.setString(2, passwordTF.getText());
            pst.setString(3, SecQ.getText());
            pst.setString(4, answerTF.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "New Account Created");
            conn.close();
            

        }catch(Exception e) {
           JOptionPane.showMessageDialog(null,e);   
        }
        }
    }//GEN-LAST:event_createMouseClicked

