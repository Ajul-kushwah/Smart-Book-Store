    private void logAdminXMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_logAdminXMouseClicked
        // TODO add your handling code here:
        if (username.getText().equals("") && password.getText().equals("")){
            javax.swing.JOptionPane.showMessageDialog(null, "Your username and password is still empty!");
            username.requestFocus();
        }else if (username.getText().equals("")  && !password.getText().equals("")){
            javax.swing.JOptionPane.showMessageDialog(null, "Your username is still empty!");
            username.requestFocus();
        }else if (password.getText().equals("")){
            javax.swing.JOptionPane.showMessageDialog(null, "Your password is still empty!");
            password.requestFocus();
        } else{
            
            conn=DatabaseConnection.ConnectDB();
        String sql="Select * from signup where username=? and password=? ";
        try{
          pst=conn.prepareStatement(sql);
          pst.setString(1,username.getText());
          pst.setString(2,password.getText());
          rs=pst.executeQuery();
          
          if(rs.next()){
              JOptionPane.showMessageDialog(null, "Login successfully..");
              this.dispose();
              Homepage home = new Homepage();
              home.setVisible(true);
          }
          else{
              JOptionPane.showMessageDialog(null, "Invalid  userneme and password ","Access Denied ",JOptionPane.ERROR_MESSAGE);
          }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
            
        }

    }//GEN-LAST:event_logAdminXMouseClicked

